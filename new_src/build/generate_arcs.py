#!/usr/bin/env python3
"""Generate arc segment bitmaps for the ring clock.

Draws 8 arc bitmaps (4 quadrants x 2 thicknesses) using Pillow and writes
each as a MicroPython-importable Python module containing a MONO_HLSB byte
array.  Run this on the host machine, not on the ESP32.

Requires: Pillow (pip install Pillow)

The generated modules are written to new_src/src/assets/arcs/.
"""

from pathlib import Path
from PIL import Image
import math
import shutil

# ---------------------------------------------------------------------------
# Ring geometry — keep in sync with constants.py on the device
# ---------------------------------------------------------------------------
RING_CENTER_X = 50
RING_CENTER_Y = 75
RING_OUTER_R = 46
RING_INNER_R_THICK = 39  # 7 px ring width
RING_INNER_R_THIN = 43   # 3 px ring width

# Quarter definitions: (label, start_angle_deg, end_angle_deg)
# Pillow measures angles from 3-o'clock going clockwise, but we want to
# define quarters starting from 12-o'clock clockwise.  Pillow's coordinate
# system: 0° = 3-o'clock, 90° = 6-o'clock, 180° = 9-o'clock, 270° = 12-o'clock.
#
# Q1: 12->3  => Pillow 270->360
# Q2:  3->6  => Pillow   0->90
# Q3:  6->9  => Pillow  90->180
# Q4:  9->12 => Pillow 180->270
QUARTERS = [
    ("q1", 270, 360),
    ("q2", 0, 90),
    ("q3", 90, 180),
    ("q4", 180, 270),
]

THICKNESSES = [
    ("thin", RING_INNER_R_THIN),
    ("thick", RING_INNER_R_THICK),
]

OUTPUT_DIR = Path(__file__).resolve().parent.parent / "src" / "assets" / "arcs"


def draw_arc_image(outer_r: int, inner_r: int, start_deg: int, end_deg: int,
                   cx: int, cy: int) -> tuple[Image.Image, int, int]:
    """Draw an arc segment and return (cropped_image, offset_x, offset_y).

    The arc is drawn on a canvas large enough to hold the full ring, then
    cropped to the quadrant bounding box.  The offsets are the top-left
    corner of the crop box in display coordinates.
    """
    canvas_w = cx + outer_r + 2
    canvas_h = cy + outer_r + 2
    img = Image.new("1", (canvas_w, canvas_h), color=1)

    for y in range(canvas_h):
        for x in range(canvas_w):
            dx = x - cx
            dy = y - cy
            dist = math.sqrt(dx * dx + dy * dy)
            if dist < inner_r or dist > outer_r:
                continue
            angle = math.degrees(math.atan2(dy, dx)) % 360
            s = start_deg % 360
            e = end_deg % 360
            if s < e:
                in_sector = s <= angle <= e
            else:
                in_sector = angle >= s or angle <= e
            if in_sector:
                img.putpixel((x, y), 0)  # black

    # Pillow's getbbox() doesn't work reliably with mode "1" images, so
    # compute the bounding box of black pixels manually.
    min_x, min_y = canvas_w, canvas_h
    max_x, max_y = 0, 0
    for y in range(canvas_h):
        for x in range(canvas_w):
            if img.getpixel((x, y)) == 0:
                min_x = min(min_x, x)
                min_y = min(min_y, y)
                max_x = max(max_x, x)
                max_y = max(max_y, y)
    if max_x < min_x:
        raise ValueError(f"Empty arc: start={start_deg}, end={end_deg}")

    bbox = (min_x, min_y, max_x + 1, max_y + 1)
    cropped = img.crop(bbox)
    return cropped, bbox[0], bbox[1]


def image_to_mono_hlsb(img: Image.Image) -> bytes:
    """Convert a 1-bit Pillow image to MONO_HLSB byte format.

    MONO_HLSB: each byte holds 8 horizontal pixels, MSB is the leftmost
    pixel.  1 = white, 0 = black.  Each row is padded to a whole number
    of bytes.
    """
    w, h = img.size
    bytes_per_row = (w + 7) // 8
    result = bytearray(bytes_per_row * h)

    for y in range(h):
        for x in range(w):
            pixel = img.getpixel((x, y))
            if pixel:  # white = 1
                byte_idx = y * bytes_per_row + x // 8
                bit_idx = 7 - (x % 8)
                result[byte_idx] |= 1 << bit_idx

    return bytes(result)


def write_module(path: Path, width: int, height: int,
                 offset_x: int, offset_y: int, data: bytes):
    """Write a Python module exporting the arc bitmap."""
    hex_lines = []
    bytes_per_row = (width + 7) // 8
    for row in range(height):
        start = row * bytes_per_row
        end = start + bytes_per_row
        row_hex = "".join(f"\\x{b:02x}" for b in data[start:end])
        hex_lines.append(f'    b"{row_hex}"')

    data_str = "\n".join(hex_lines)

    contents = f"""\
# Auto-generated by build/generate_arcs.py — do not edit by hand.
WIDTH = {width}
HEIGHT = {height}
OFFSET_X = {offset_x}
OFFSET_Y = {offset_y}
DATA = (
{data_str}
)
"""
    path.write_text(contents)
    print(f"  wrote {path.name} ({width}x{height}, offset=({offset_x},{offset_y}), {len(data)} bytes)")


def main():
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    # Write __init__.py
    init_path = OUTPUT_DIR / "__init__.py"
    if not init_path.exists():
        init_path.write_text("")

    print("Generating arc bitmaps...")
    for q_name, start_deg, end_deg in QUARTERS:
        for t_name, inner_r in THICKNESSES:
            img, ox, oy = draw_arc_image(
                RING_OUTER_R, inner_r, start_deg, end_deg,
                RING_CENTER_X, RING_CENTER_Y,
            )
            data = image_to_mono_hlsb(img)
            filename = f"{q_name}_{t_name}.py"
            write_module(OUTPUT_DIR / filename, img.width, img.height, ox, oy, data)

    pycache = OUTPUT_DIR / "__pycache__"
    if pycache.exists():
        shutil.rmtree(pycache)
        print("  removed __pycache__")

    print("Done.")


if __name__ == "__main__":
    main()
